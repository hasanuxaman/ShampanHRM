                if (currConn.State != ConnectionState.Open)
                {
                    currConn.Open();
                }

                #endregion open connection and transaction

                #region sql statement
                sqlText = "";
                sqlText = @"SELECT Id, Name    FROM OtherInfo ";
                sqlText += @" WHERE Name like '%" + term + "%' and IsArchive=0 and IsActive=1";

                if (!string.IsNullOrWhiteSpace(infoType))
                {
                    sqlText += @" and InfoType=@infoType";
                }
                sqlText += "";
                SqlCommand _objComm = new SqlCommand(sqlText, currConn);
                if (!string.IsNullOrWhiteSpace(infoType))
                {
                    _objComm.Parameters.AddWithValue("@infoType", infoType);
                }

                SqlDataReader dr;
                dr = _objComm.ExecuteReader();
                int i = 0;
                while (dr.Read())
                {
                    vms.Insert(i, dr["Name"].ToString());
                    i++;
                }
                dr.Close();
                vms.Sort();
                #endregion
            }
            #region catch


            catch (SqlException sqlex)
            {
                throw new ArgumentNullException("", "SQL:" + sqlText + FieldDelimeter + sqlex.Message.ToString());
            }
            catch (Exception ex)
            {
                throw new ArgumentNullException("", "SQL:" + sqlText + FieldDelimeter + ex.Message.ToString());
            }

            #endregion
            #region finally

            finally
            {
                if (currConn != null)
                {
                    if (currConn.State == ConnectionState.Open)
                    {
                        currConn.Close();
                    }
                }
            }

            #endregion

            return vms;
        }

